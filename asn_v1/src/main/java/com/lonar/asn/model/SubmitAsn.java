package com.lonar.asn.model;

public class SubmitAsn {
	private Long shipmentHeaderId;
	private String status;
	private Long lastUpdateLogin;
	private Long acceptedBy ;
	
	public Long getShipmentHeaderId() {
		return shipmentHeaderId;
	}
	public void setShipmentHeaderId(Long shipmentHeaderId) {
		this.shipmentHeaderId = shipmentHeaderId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Long getLastUpdateLogin() {
		return lastUpdateLogin;
	}
	public Long getAcceptedBy() {
		return acceptedBy;
	}
	public void setAcceptedBy(Long acceptedBy) {
		this.acceptedBy = acceptedBy;
	}
	public void setLastUpdateLogin(Long lastUpdateLogin) {
		this.lastUpdateLogin = lastUpdateLogin;
	}
	@Override
	public String toString() {
		return "SubmitAsn [asnHeaderId=" + shipmentHeaderId + ", status=" + status + ", lastUpdateLogin=" + lastUpdateLogin
				+ "]";
	}
	
	

}
