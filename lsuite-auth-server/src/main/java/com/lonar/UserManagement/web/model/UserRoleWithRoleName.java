package com.lonar.UserManagement.web.model;

public class UserRoleWithRoleName {
	private LtMastUserRoles ltMastUserRoles;
	private String roleName;
	private String roleDesc;

	public UserRoleWithRoleName(LtMastUserRoles ltMastUserRoles, String roleName, String roleDesc) {

		this.ltMastUserRoles = ltMastUserRoles;
		this.roleName = roleName;
		this.roleDesc = roleDesc;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getRoleDesc() {
		return roleDesc;
	}

	public void setRoleDesc(String roleDesc) {
		this.roleDesc = roleDesc;
	}

	public LtMastUserRoles getLtP2pUserRoles() {
		return ltMastUserRoles;
	}

	public void setLtP2pUserRoles(LtMastUserRoles ltMastUserRoles) {
		this.ltMastUserRoles = ltMastUserRoles;
	}

}
